#!/bin/bash

SLAVE_NAME=slave
CONTAINER_VERSION=latest
MASTER_HOST=localhost
MASTER_PORT=8080
#JENKINS_SECRET=<secret>
JENKINS_SSL=false

docker stop $SLAVE_NAME
docker rm $SLAVE_NAME

docker run --rm -e "MASTER_HOST=$MASTER_HOST" -e "MASTER_PORT=$MASTER_PORT" -e "JENKINS_SSL=$JENKINS_SSL" -e "SLAVE_NAME=$SLAVE_NAME" -e "JENKINS_SECRET=$JENKINS_SECRET" --name $SLAVE_NAME eficode/jenkins-slave:$CONTAINER_VERSION
